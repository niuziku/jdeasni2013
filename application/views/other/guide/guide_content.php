        <h3>如何在定制牛仔裤？</h3>
      	
        <h4>第一步</h4>
        <p>浏览网站内容或者导航栏目，挑选合适商品。</p>
        <img class="thumbnail" src="<?php echo base_url('images/others/guide_1.png');?>" />
        
        <h4>第二步</h4>
        <p>进入商品详情页，确定定制产品细节，若无需额外变更，请点击左边的“如图”框框<br />
			若需要指定更多细节，请点击“更多细节选择”</p>
        <img class="thumbnail" src="<?php echo base_url('images/others/guide_2.png');?>" />
        
        <h4>第三步</h4>
        <p>点击页面右侧的尺码框，确定尺码</p>
        <ol>
            <li>标准尺码：确定版型后，定制腰围及内长</li>
            <li>合身定制：根据测量示意，填写局部具体尺码定制</li>
            <li>来样定制：寄送合适裤子给我们作尺寸参考（推荐）</li>
        </ol>
        <p>标准尺码的单位为英寸，测量过程中若有问题请随时点击页面右上方的“联系客服”按钮与客服联系</p>
        <img class="thumbnail" src="<?php echo base_url('images/others/guide_3.png');?>"/>
        
        <h4>第四步</h4>
        <p>在购物车中确认商品的数量及金额等信息无误后，点击“立即购买”进行结算。如果还需要其他商品，可点击“继续购物”，继续挑选商品。若需要定制多一条同款的产品，请点击右上角的“加一条”按钮</p>
        <img class="thumbnail" src="<?php echo base_url('images/others/guide_4.png');?>"/>

        <h4>第五步</h4>
        <p>填写订单信息，并选择支付方式（使用担保交易方式，先支付到第三方，收货满意后再放款）。若有特殊要求需要注意的，请在留言框中写明。</p>
        <p>订单信息甚为重要，请认真填写，若在制作过程中出现问题，我们将以电话方式联系。</p>
